#include "ft_printf.h"

void	ft_putstr(char *str, int *len)
{
	int	i;
	
	i = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		*len += 6;
	}
	else
	{
		while (str[i])
		{
			ft_putchar(str[i], len);
			i++;
		}
	}
}
